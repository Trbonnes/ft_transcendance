<template>
  <div class="flex flex-col h-full" >
    <div id="inner" class="flex flex-col">
      <div v-if="messages.length === 0" class="flex flex-row iterms-center justify-center"><span class="text-gray-400">Say hello to your new friend !</span></div>
      <div v-for="c in messages" v-bind:key='c.id'
            class="p-3 m-0.5 rounded-xl inline-block max-w-full break-all"
            v-bind:class="[$auth && $auth.user && $auth.user.id === c.senderId ? 'text-white bg-blue-500 self-end' : ' text-black bg-gray-300 self-start']">
        {{ c.content }}
      </div>
    </div>
    <div class="flex flex-row">
      <input
        ref="textinput"
        type="text"
        @keyup="listenKey"
        v-model="message"
        class="flex-grow input input-bordered"
      />
      
      <span @click="sendMessage" class="btn btn-primary">Send</span>
    </div>
  </div>
</template>

<script lang="ts">
import Vue from 'vue'

export default Vue.extend({
  props: ['messages'],
  data() {
    return {
      message: '',
    }
  },
  updated() {
    this.scrollBottom()
  },
  mounted() {
    this.scrollBottom()
    if(this.$refs.textinput)
      (this.$refs.textinput as any).focus()
  },
  computed: {},
  methods: {
    // urlify(text) {
    //   var urlRegex = /(https?:\/\/[^\s]+)/g;
    //   return text.replace(urlRegex, '<a href="$1">$1</a>')
    // },
    listenKey(e: any) {
      // key code for enter key
      if (e.keyCode === 13) this.sendMessage()
    },
    sendMessage() {
      if (this.message.trim() !== '') {
        // sending the even to the parent page
        this.$emit('sendMessage', this.message.trim())
        this.message = ''
      }
    },
    scrollBottom()
    {
      var container = this.$el.querySelector("#inner");
      if (container)
        container.scrollTop = container.scrollHeight;
    }
  },
})
</script>

<style>

#inner {
  height: 70vh;
  width: 100%;
  overflow-y: scroll;
}
</style>
